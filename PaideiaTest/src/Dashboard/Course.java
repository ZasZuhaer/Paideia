public class Course{
    String title;
    String category;
    String description;
    int price;
    String instructor_name;
    public Course(String title, String category, String description, int price, String instructor_name){
        this.title = title;
        this.category = category;
        this.description = description;
        this.price = price;
        this.instructor_name = instructor_name;
    }
}